
public abstract class Problem {
    String prompt;
    String answer;
    
    public String getPrompt() {
    	return prompt;
    }

    public String getAnswer() {
    	return answer;
    }

    public void setPrompt(String prompt) {
    	this.prompt = prompt;
    }

    public void setAnswer(String answer) {
    	this.answer = answer;
    }
}
